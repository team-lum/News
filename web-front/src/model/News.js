class News {

    constructor(author,
                title,
                url,
                urlToImage,
                publishedAt,
                content) {
        this.author = author;
        this.title = title;
        this.url = url;
        this.urlToImage = urlToImage;
        this.publishedAt = publishedAt;
        this.content = content;
    }

}

export default News;